// Function to handle file uploads
function handleFileUpload(event) {
	const files = event.target.files; // Get the selected files
  
	// Loop through the selected files
	for (let i = 0; i < files.length; i++) {
	  const file = files[i];
	  const reader = new FileReader(); // Create a FileReader object
  
	  // Read the file as a data URL
	  reader.readAsDataURL(file);
  
	  // Callback function executed when the FileReader has finished reading the file
	  reader.onload = function () {
		const imgSrc = reader.result; // Get the data URL
  
		// Create an image element and set its source
		const img = document.createElement("img");
		img.src = imgSrc;
  
		// Create a container for the image
		const container = document.createElement("div");
		container.classList.add("image-item");
		container.appendChild(img);
  
		// Create a delete button for the image
		const deleteButton = document.createElement("button");
		deleteButton.classList.add("delete-button");
		deleteButton.innerHTML = "x";
  
		// Add a click event listener to the delete button
		deleteButton.addEventListener("click", function () {
		  container.remove(); // Remove the image container when the delete button is clicked
		});
  
		// Append the delete button to the image container
		container.appendChild(deleteButton);
  
		// Append the image container to the imageContainer
		document.getElementById("imageContainer").appendChild(container);
	  };
	}
  }
  
  // Add an event listener to the file upload input
  document.getElementById("upload").addEventListener("change", handleFileUpload);
